// This is one of the Arithmetic Operators
/*
Exponentiation means raising a certain base number to the power of the exponent.
A good example is x to the power of y. It means that we multiply x by itself
y number of times. Let's take a look at the example below.
*/

let nr1 = 2;
let nr2 = 3;
result = nr1 ** nr2;
console.log(result);

/*
Now we can also find the square root of a number using fractional
exponentiation, that is raising the number to 0.5. Example;
*/

let nr3 = 16;
let sqrtNr3 = nr3 ** 0.5;
console.log(sqrtNr3);