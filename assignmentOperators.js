/*
We've worked with arithmetic operators and even worked with an assignment
operator when assigning values to variables, it is the "=" operator.
Every binary arithmetic operator has an assignment operator to write a short
piece of code for example x += 5 means x = x + 5 and x **= 3 means 
x raised to the power of three. Let's look at more specific examples below.

So we are going to create a variable called x and assign it a value of 2
then we will work with different binary arithmetic operators to see how
they affect it.
*/

let x = 2;
x += 2; // this makes 4 the new value of x
x -= 2; // makes 2 the new value of x
x *= 6; // makes 12 the new value of x
x /= 3; // makes 4 the new value of x
x **= 2 // makes 16 the new value of x
x %= 3; // makes the value of x 1