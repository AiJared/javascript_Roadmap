// Miles-to-kilometers converter
/*
Create a variable that contains a value in kilometers, 
convert it to miles, and log the
value in kilometers in the following format:
"The distance of 130 kms is equal to 
80.77845576447488 miles"
For reference, 1 mile equals 1.60934 kilometers.
*/

let kms = 130;
let miles = kms / 1.60934;
console.log("The distance of 130 kms is equal to " + miles + " miles.");
